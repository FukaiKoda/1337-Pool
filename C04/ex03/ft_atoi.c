int	ft_atoi(char *str)
{
	int		num;
	int		sign;

	num = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 15) || *str == ' ')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = (*str - '0') + (num * 10);
		str++;
	}
	return (num * sign);
}
