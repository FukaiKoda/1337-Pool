#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	nbl;
	int		mod;

	nbl = nb;
	if (nbl < 0)
	{
		nbl *= -1;
		write(1, "-", 1);
	}
	if (nbl > 9)
		ft_putnbr(nbl / 10);
	mod = (nbl % 10) + 48;
	write(1, &mod, 1);
}
